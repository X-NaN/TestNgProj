public class CharUtils {

    /**
     * 将字符转换成ASCII码值
     * @param ch
     * @return
     */
    public static int charToASCII(final char ch){
        return (int)ch;
    }

    /**
     * 将ASCII码值转换成字符
     * @param ascii
     * @return
     */
    public static char asciiToChar(final int ascii){
        return (char)ascii;
    }
}
